/// <summary> Determines how String Case should be Handled. </summary>

public enum StringCase
{
/// <summary> The String will use the <b>Default</b> Case. </summary>
Default,

/// <summary> The Strings will be in <b>lowercase</b>. </summary>
Lower,

/// <summary> The Strings will be in <b>UPPERCASE</b>. </summary>
Upper
}